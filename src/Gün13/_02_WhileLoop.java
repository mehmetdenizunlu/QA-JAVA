package Gün13;

public class _02_WhileLoop {
    public static void main(String[] args) {
        // ekrana 5 kez merhaba yazdırınız.

        int sayac = 0;

        while (sayac<5) {
           // şart sağlanıyorsa parantezlerini arasını çalıştır
            System.out.println("merhaba");
            sayac++;
        }
    }
}
