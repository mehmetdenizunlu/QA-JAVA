package Gün08;

public class _01_JavaAritmeticOperators {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;

        System.out.println("Toplama İşlemi : " + (a+b));
        System.out.println("Çıkartma İşlemi : " + (a-b));
        System.out.println("Çarpma İşlemi :" + (a*b));
        System.out.println("Bölme İşlemi : " + (a/b));
        System.out.println("Mod Alma İşlemi : " + (a%b));
    }
}
