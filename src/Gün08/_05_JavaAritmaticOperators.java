package Gün08;

public class _05_JavaAritmaticOperators {
    public static void main(String[] args) {
        int a = 10;

        a=a+5; // a değerini 5 arttır demek
        a+=5; // buda aynı ifade a yı 5 arttır demek

        a=a-5; // a değerini 5 azalt demek
        a-=5; // buda aynı şeyi ifade eder a yı 5 azalt demek

        a=a*5; // a değerini 5 ile çarpar
        a*=5; // buda aynı

        a=a/5; // a değerini 5 e böl
        a/=5; // buda aynı


    }
}
